// Parses upstream Symboleo.xtext and emits src/editor/symboleoac.monarch.ts.
// Run via `npm run gen:monarch` (also invoked from `predev` and `prebuild`).
//
// Strategy: pull every quoted literal from the grammar. Bucket them into
//   - keywords    (alpha-only, plain identifiers like `Domain`, `Obligation`)
//   - typeKeywords (multi-word with `.` — `Math.pow`, `String.concat`, …)
//   - operators    (non-alphanumeric — `==`, `<=`, `->`, `:=`, …)
//   - controlKeywords (`with`, `is`, `isA`, `isAn`, `or`, `and`, `not`, `to`, `by`, `on`)
// Then write a Monarch tokenizer that uses them.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const GRAMMAR = resolve(HERE, '../../upstream/SymboleoAC-IDE/ca.uottawa.csmlab.symboleo/src/ca/uottawa/csmlab/symboleo/Symboleo.xtext');
const OUT = resolve(HERE, '../src/editor/symboleoac.monarch.ts');

const raw = readFileSync(GRAMMAR, 'utf8');

// Strip line comments and block comments so we never pull strings out of them.
const stripped = raw
  .replace(/\/\*[\s\S]*?\*\//g, '')
  .replace(/\/\/[^\n]*/g, '');

const literals = new Set();
const re = /'([^'\\]*(?:\\.[^'\\]*)*)'|"([^"\\]*(?:\\.[^"\\]*)*)"/g;
let m;
while ((m = re.exec(stripped)) !== null) {
  const lit = m[1] ?? m[2];
  if (!lit) continue;
  literals.add(lit);
}

const all = [...literals].sort();
const isAlphaWord = (s) => /^[A-Za-z][A-Za-z0-9]*$/.test(s);
const isDotted = (s) => /^[A-Za-z][A-Za-z0-9]*(?:\.[A-Za-z][A-Za-z0-9]*)+$/.test(s);
const isLower = (s) => /^[a-z]+$/.test(s);

// Words that read as control flow / connectives — lowercase verbs/connectors.
const CONTROL = new Set(['with', 'is', 'isA', 'isAn', 'or', 'and', 'not', 'To', 'On', 'by', 'self']);

const controlKeywords = [];
const keywords = [];
const builtinFunctions = [];
const operators = [];

for (const lit of all) {
  if (isDotted(lit)) {
    builtinFunctions.push(lit);
    continue;
  }
  if (isAlphaWord(lit)) {
    if (CONTROL.has(lit)) controlKeywords.push(lit);
    else keywords.push(lit);
    continue;
  }
  operators.push(lit);
}

const body = `// AUTO-GENERATED by scripts/extract-keywords.mjs from
// upstream/SymboleoAC-IDE/.../Symboleo.xtext. Do not edit by hand.
// Re-run \`npm run gen:monarch\` after grammar changes.

import type { languages } from 'monaco-editor';

export const SYMBOLEOAC_LANGUAGE_ID = 'symboleoac';
export const SYMBOLEOAC_FILE_EXTENSION = '.symboleo';

export const symboleoacKeywords: string[] = ${JSON.stringify(keywords, null, 2)};

export const symboleoacControlKeywords: string[] = ${JSON.stringify(controlKeywords, null, 2)};

export const symboleoacBuiltins: string[] = ${JSON.stringify(builtinFunctions, null, 2)};

export const symboleoacOperators: string[] = ${JSON.stringify(operators, null, 2)};

export const symboleoacLanguageConfiguration: languages.LanguageConfiguration = {
  comments: { lineComment: '//', blockComment: ['/*', '*/'] },
  brackets: [['{', '}'], ['[', ']'], ['(', ')']],
  autoClosingPairs: [
    { open: '{', close: '}' },
    { open: '[', close: ']' },
    { open: '(', close: ')' },
    { open: '"', close: '"' },
    { open: "'", close: "'" },
  ],
  surroundingPairs: [
    { open: '{', close: '}' },
    { open: '[', close: ']' },
    { open: '(', close: ')' },
    { open: '"', close: '"' },
    { open: "'", close: "'" },
  ],
};

export const symboleoacMonarchLanguage: languages.IMonarchLanguage = {
  defaultToken: '',
  ignoreCase: false,
  keywords: symboleoacKeywords,
  controlKeywords: symboleoacControlKeywords,
  builtins: symboleoacBuiltins,
  // Symbols handled per-token in the tokenizer below.

  tokenizer: {
    root: [
      // Whitespace + comments
      { include: '@whitespace' },

      // Dotted built-ins (Math.pow, String.concat, …) — match BEFORE identifiers
      [/[A-Za-z][\\w]*(?:\\.[A-Za-z][\\w]*)+/, {
        cases: {
          '@builtins': 'predefined',
          '@default': 'identifier',
        },
      }],

      // Identifiers — bucket against keyword tables
      [/[A-Za-z_][\\w]*/, {
        cases: {
          '@controlKeywords': 'keyword.control',
          '@keywords': 'keyword',
          '@default': 'identifier',
        },
      }],

      // Numbers
      [/\\d+\\.\\d+/, 'number.float'],
      [/\\d+/, 'number'],

      // Strings (double + single)
      [/"([^"\\\\]|\\\\.)*"/, 'string'],
      [/'([^'\\\\]|\\\\.)*'/, 'string'],

      // Multi-character operators
      [/:=|==|!=|>=|<=|->/, 'operator'],

      // Single-character operators / punctuation
      [/[{}()[\\]]/, '@brackets'],
      [/[;,.]/, 'delimiter'],
      [/[+\\-*/%<>!]/, 'operator'],
      [/:/, 'operator'],
    ],

    whitespace: [
      [/[ \\t\\r\\n]+/, ''],
      [/\\/\\*/, 'comment', '@comment'],
      [/\\/\\/.*$/, 'comment'],
    ],

    comment: [
      [/[^/*]+/, 'comment'],
      [/\\*\\//, 'comment', '@pop'],
      [/[/*]/, 'comment'],
    ],
  },
};
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, body, 'utf8');
console.log(`extracted ${all.length} literals → ${OUT}`);
console.log(`  keywords: ${keywords.length}, control: ${controlKeywords.length}, builtins: ${builtinFunctions.length}, operators: ${operators.length}`);
